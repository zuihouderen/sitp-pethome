        <p>1、灰黄霉素要慎用</p>
        <p>尤其是一岁以下未成年的小猫，因为所有杀真菌的口服药对肝脏的损害非常大，将来会造成猫咪肝功能紊乱，留下病根。如果猫咪不是全身大面积的猫癣，就不要轻易服用灰黄霉素或其他杀真菌的口服药。即使猫咪患有全身的严重猫癣，服用灰黄霉素时要捣碎了或碾成粉，用食用油掺和着喂下去，而且最好是喂一天停一天这样间隔开来喂，即使这样服用的时间也不能超过2周。</p>
        <p>2、要有耐心</p>
        <p>猫咪患上猫癣后恢复情况跟猫咪本身的体质有很大关系。原来身体条件的好的猫，康复的周期短，原来身体条件不好的猫，康复时间长，而且可能很长(超过2个月或更长时间)。但是无论如何，治疗要坚持要有耐心。千万不可因为猫咪患了猫癣，大面积脱毛，变丑了，就抛弃他，猫癣绝对不是致命的病，从根本上说对猫咪的健康影响不大，所以一定要耐心。</p>
        <p>3、补充营养</p>
        <p>猫咪患上猫癣以后，除了外用克霉唑之类的药物外，很重要的一点就是补充营养，多晒太阳。这期间应该加强猫粮的营养，(如选用法国皇家的幼猫猫粮)，另外给猫咪服用动物用的维生素补充品“叭咪宝”，(或者把小儿善存，小施尔康之类儿童用的维生素药碾碎了拌在猫罐头里，效果相同，每天一颗)。</p>
        <p>补充药物服用时间可以长一点，3、4个月或者半年，即使猫癣好了以后也可以继续喂，增强体质。还要尽可能地让猫咪多晒太阳。</p>
        <p>4、勤消毒</p>
        <p>家中环境要勤消毒，尤其是猫咪常睡的窝，垫子，每天用按要求稀释后的宠物环境消毒液喷洒，家中的地板，家具要隔天或隔两天用稀释后的消毒液擦拭，能够有效的防止猫癣的扩散。</p>
        <p>5、及时发现</p>
        <p>已经出现在猫咪身上的大面积脱毛，当然是重度猫癣的特征，除了对这些部位搽克霉唑之类的外用药物以外，还一定要注意及时发现猫咪身上其他部位的新长出来的猫癣。做法是每天给猫咪上完药之后，顺着毛，用双手紧贴着猫咪的皮肤从脑袋到尾巴仔仔细细的摸一遍，如果手触及到硬硬的小疙瘩或小突起，翻开来毛来看看，可以发现一小块结痂，旁边还有一小撮皮屑，那就是新长的猫癣，这些部位千万别忘了也要上药，如果等到这些部位也发展成大面积脱毛，治疗就更难了。</p>
