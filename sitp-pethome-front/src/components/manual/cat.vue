        <p>第一类是传染疾病：</p>
        <p>秋季好发的传染病主要是猫瘟，猫瘟的学名為泛白血球减少症，是由细小病毒科病毒引起的猫白血球急剧减少的一种急性传染病，其死亡率高且病程长。在秋季猫的体质较差，而此种致病的病毒在此气候活性最强，所以未接种过猫瘟疫苗的猫即使是足不出户也可能感染本病。</p>
        <p>被感染的猫主要表现為消化道症状，剧烈呕吐，继而伴发腹泻、高热，食欲全无，精神极度沉鬱，后期可能出现血便，病程一般会持续一周左右。停止呕吐且有 食欲则為康复的表现，此病的自然耐过率很低，如果您的爱猫出现呕吐、腹泻和体温升高，就应立即带牠到医院就诊。如果血液化验白血球在6000以下就基本可确诊本病，如果白血球在2000以上，治疗及时，其治癒率在85%以上。</p>
        <p>第二类呼吸道疾病：</p>
        <p>体弱、老年猫及纯种猫易发，此类病包括鼻炎、气管炎及支气管炎、肺炎。鼻炎的症状為持续鼻涕，一侧或双侧鼻孔有脓性鼻液流出，但没有咳嗽症状，不影响精神 和食欲。此病為慢性，一般消炎治疗效果不好，但部分可自癒，进一步发展可变為气管及支气管炎，症状為咳嗽，有时有鼻涕，严重时可导致体温升高，影响精神和 食欲。如果及时治疗可在一周左右治癒，如治疗不及时或体质过差，可能转為慢性或进一步发展為肺炎。</p>
        <p>肺炎一般有体温升高等全身症状，如果是大叶性肺炎可危及生命。此类病的预防主要注意环境冷热变化，减少给猫咪洗澡的次数，洗澡也要挑选相对暖和的时间，发情的猫不要放到户外。</p>
        <p>第三类是猫的老年病：</p>
        <p>同老人一样，天气冷，特别是初冬季节，肝功能障碍，肾功能衰竭，突发性心臟病及糖尿病的发病率都较其他季节会有显著提高的趋势。所以秋季应特别注意老年猫有无异常情况，如果发现应及时到医院治疗。</p>
        <p>在夏末就应该给猫加强营养，增强体质，秋季到来时应减少外界刺激及发情对它们的影响。按时接种疫苗，仔细观察它们的行為，这样才能使您的爱猫健康地度过秋季。</p>
