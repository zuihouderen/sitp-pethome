        <p>找放大镜一根(看报纸的就行)，仔细观察猫咪耳朵里红褐色的地方，有红褐色蚜虫似的小虫子在爬动，那就是耳螨了。洗耳液除了可以清洁耳道，还可以消炎，预防/治疗因为耳螨造成的中耳炎。耳油是杀虫的，但有毒，注意不要让猫咪去舔。</p>
