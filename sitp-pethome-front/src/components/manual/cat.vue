        <p>多尿 脱水 多饮 排尿失禁 食欲下降</p>
        <p>【 病因：】</p>
        <p>尿崩症是由加压素合成或释放完全或部分障碍引起慢性尿崩症(CDI)，或者是由肾小管对加压素的反应完全或部分障碍引起肾性尿崩症(NDI)。两种类型的尿崩症都可以是先天性形成也可以是后天获得的。CDI可能由瘤形成或脑创伤引起，但一般在猫，该病都是先天形成的。炎症性或寄生虫引起的cDI很少见。</p>
        <p>【 症状描述：】</p>
        <p>临床表现尿崩症的症状包括明显多尿症。常表现出遗尿症和失禁，并表现严重的烦渴症状，有时24h饮水达200ml/kg，在后天病例中，动物突然表现临床 症状。患病猫寻找水源，表现厌食，体重逐渐减轻。</p>
        <p>尽管渴感越发强烈，但患病猫表现轻度到中度脱水。患病猫一般不表现其他临床症状，虽然有时神 经症状很明显，特别是尿崩症伴发肿瘤，如垂体肿瘤。</p>
        <p>【疾病诊断：】</p>
        <p>尿崩症必须和原发性烦渴伴发多尿病，也即心理性烦渴(PP)区分开来。在PP中，因过度饮水加压素的分泌相对较低，而且，因为肾脏要处理大量液体，使髓质张力降低，进而肾脏浓缩能力降低。</p>
        <p>【 疾病治疗：】</p>
        <p>对患扩散性下丘脑或垂体瘤的患病猫，特别是表现出神经症状的，愈后不良。脑部有创伤的愈后情况各异，有些病例会在几天或几周内自发痊愈，有些刨伤是永久性的。NDI愈后不良，为了采用更好的治疗办法，应诊断其原发性病因。</p>
