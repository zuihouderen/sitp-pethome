        <p>【1、颜色不同。】</p>
        <p>耳螨是深褐色的，如果仔细看，可以看到褐色或浅褐色的耳螨虫(用放大镜)。</p>
        <p>【2、污垢性状不同。】</p>
        <p>有耳螨的猫咪耳朵分泌很多类似油脂的褐色液体。耳螨垢呈不规则片状，贴在猫耳朵上。</p>
        <p>如果是泥的话，其一，分布应该均匀，不是一块一块的。其二：不应该有大量褐色液体分泌物。</p>
        <p>【3、触觉不同。】</p>
        <p>取一小块耳垢，用手指捻一下，泥很容易捻匀，感觉细腻。耳螨垢感觉粗糙，不容易捻开。</p>
