        <p>猫被人盯着时它会感觉到被攻击的威胁，因此而紧张或生气。所以当你要与猫面对的时候，你一定要让脸上堆满灿烂的笑容，并尽量把眼睛眯起来或把视线移开。</p>
        <p>三、猫在睡觉时讨厌别人打扰</p>
        <p>猫每天占三分之二的时间都在睡觉，如果你想在猫睡觉的时候去和它亲热或逗其玩，那就大错特错了，其实此刻它对你的亲昵动作是极不耐烦和恨之入骨的!最后一点、年纪较老的的猫喜欢安静。当它远离你和呆在安静的环境时，你最好不要去打扰它!</p>
        <p>猫讨厌什么，主人最好要掌握清楚，尤其是家庭有小孩的，要加强对孩子的教育，不要因为孩子无意间的一些行为招惹了猫猫，而引致孩子被猫猫伤害的情况。</p>
