        <p>狗狗咳嗽的几种情况：</p>
        <p>普通咳嗽。狗狗和人一样，当兴奋、睡醒、受惊、食物呛等原因都会导致狗狗咳嗽，这种咳嗽一般短促而短暂，没有其他症状，完全不用担心。</p>
        <p>环境的突然改变导致狗狗咳嗽。从暖空气环境进入冷空气环境，烟雾、灰尘等微细颗粒刺激呼吸道，呛鼻气味(例如氨气，辣椒烟雾等)，慢性致病空气环境(例如新居的油漆味)，等等，都会导致狗狗咳嗽。如果是短暂的环境因素影响，一般不会出现连续长期的咳嗽，受到比较强烈刺激时，会伴有流鼻涕和流泪的情况，脱离有害环境就会好转和消失。</p>
        <p>狗狗长期生活在有害环境(例如甲醛超标环境)，那可能会出现长期连续干咳，此时呼吸道和肺部可能都已经出现病变了，需要找兽医。如果狗狗受到比较强烈的呼吸道刺激，例如浓氨气刺激，有可能灼伤呼吸道，这种情况会发炎，不能光靠转移环境，还需要找兽医进行专门治疗。</p>
        <p>狗狗有时候吃骨头鱼刺，或是撕咬一些多刺物品，都可能被异物卡住喉咙或是刺到牙龈，狗狗会自己主动咳嗽，希望把刺给咳出来。这种情况狗狗往往一边咳嗽，一边用爪子自己抓嘴巴，还不时短促尖锐地叫，很容易判断的。狗狗一般都不会被刺卡住，最终都能自己解决。如果狗狗自己解决不了，就一定要找兽医拔刺，防止中刺部位发炎。</p>
        <p>某些毒物中毒，也会导致狗狗咳嗽。这种情况，咳嗽短促平和，往往还伴有呕吐和白沫，白沫不粘稠，只要不存在肌无力和精神不振，一般喂点葡萄糖口服液或是肌苷口服液，即可没事。</p>
        <p>呼吸道炎症、肺炎、肠胃炎等病变，也会导致狗狗咳嗽。这种病变导致的咳嗽，往往是长期连续咳嗽，有的情况下是尖锐干咳，眼鼻有粘结物，腹泻呕吐，呕吐物比较粘稠,毛色暗淡，精神萎靡，食欲不振。这种情况要及早找兽医治疗。</p>
        <p>狗狗也有时候容易上火，也会导致咳嗽。这种情况出现的是干咳，往往会眼角有分泌物，眼睛有比较多血丝，便干，无其他明显的症状。这种情况，可以买点蛇胆川贝枇杷露或是牛黄清肺散喂一下即可。</p>
        <p>一些犬只在狂犬病发作前期，也会出现咳嗽。这种情况，狗狗会出现夹尾巴，到处乱窜，恐水，狂吠，吐白沫，眼光异样。由于现阶段狂犬病无有效疗法，一旦遇到这样情况，确认是狂犬后，要立即扑杀，防止狂犬病流行。</p>
