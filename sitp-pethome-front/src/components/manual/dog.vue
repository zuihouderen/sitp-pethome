        <p>白内障是发生在狗狗眼球晶状体的疾病，是狗狗眼睛常见病里的一个比较严重的，因为它会引起狗狗的眼睛失明。得白内障的原因也挺多，有先天的原因，也有因为年纪大了的原因，或是受外伤以后引起的等等。刚开始得白内障的时候，狗狗并没有明显的视力不好的情况，随着晶状体的透明性减弱，慢慢视力就会受影响了。如果狗狗的年纪比较大了，那么在平时就要注意观察狗狗的眼球，如果发现没有以前亮了或是清澈了，就要有这个意识狗狗有可能得了白内障。</p>
